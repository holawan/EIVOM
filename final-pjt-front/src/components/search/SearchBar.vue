<template>
  <div >
    <div class="search__container">
      <div style="background-color:white; height:200px;"></div>
      <p class="search__title">
          궁금한 영화를 검색해보세요 !
      </p>
      <input @input="onInputText" @keyup.enter="onSearchText" class="search__input" type="text" placeholder="Search">
    </div>

  </div>
</template>

<script>
export default {
  name: 'SearchBar',
  methods: {
    onInputText (event) {
      const searchKeyword = event.target.value.trim()
      if (searchKeyword) {
        this.$emit('text-input', searchKeyword)
        }
    },
    onSearchText (event) {
      const search = event.target.value.trim()
      this.$emit('search-text',search)
      event.target.value=''
    },
  },
  
}
</script>

